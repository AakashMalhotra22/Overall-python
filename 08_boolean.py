'''
Boolean
1. These are operators that allow you to convey True or False statements.
2. It is represented by bool()
3. 0 means False
4. Anything other than 0 is True.
5. list, dict, strings, tuple, set and range cannot be converted into bool.
6. int, float and None can be converted into bool.
7. None means False in bool.
'''

