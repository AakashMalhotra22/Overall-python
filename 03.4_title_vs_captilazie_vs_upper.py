'''
1. upper capitalises each letter in the sentence.
2. title capitalises first letter of each word in the sentence.
3. capitalize capitalises very first letter of sentence.
'''

y1 ="ram is a good boy."
print(y1.upper())
print(y1.title())
print(y1.capitalize())

print(2*'\n')

y2 = "ram12aakash"
print(y2.upper())
print(y2.title())
print(y2.capitalize())
